package br.com.etecia.lunchbox;

public class FoodItem {
    private int id;
    private String nome;
    private String descricao;
    private String imagem_url;

    // Getters e setters
    public int getId() {
        return id;
    }

    public String getName() {
        return nome;
    }

    public String getDescription() {
        return descricao;
    }

    public String getImageUrl() {
        return imagem_url;
    }
}