package br.com.etecia.lunchbox;

public class Alimentos {
    private int id;
    private String nome;
    private String descricao;
    private String imagemUrl;

    // Getters e setters
    public int getId() {
        return id;
    }

    public String getName() {
        return nome;
    }

    public String getDescription() {
        return descricao;
    }

    public String getImageUrl() {
        return imagemUrl;
    }
}